const mongoose = require("mongoose");
let usersSchema = mongoose.Schema({
    first_name: {
        type: String,
        required: true
    },
    last_name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    username: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    address : [{type: mongoose.Schema.Types.ObjectId,ref:'Address'}]
});
module.exports = mongoose.model('User', usersSchema);